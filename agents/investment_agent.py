from langchain_openai import ChatOpenAI
from util.agent_state import AgentState
from langchain_core.messages import HumanMessage
from tools.prompt_templates import INVESTMENT_PROMPT


# 투자 판단 에이전트 생성
def create_investment_agent(state: AgentState) -> dict[str, list]:
    financial_information = state['financial'][-1]
    market_information = state['market'][-1]
    tech_information = state['tech'][-1]
    ceo_information = state['ceo'][-1]

    model = ChatOpenAI(temperature=0.5, model="gpt-4o-mini")

    chain = INVESTMENT_PROMPT | model

    res = chain.invoke({
        "financial_information": financial_information,
        "market_information": market_information,
        "tech_information": tech_information,
        "ceo_information": ceo_information
    })


    return {"investment": [res]} 


